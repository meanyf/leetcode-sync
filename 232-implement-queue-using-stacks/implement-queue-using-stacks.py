# @lc code=start
class MyQueue:

    def __init__(self):
        self.stack1 = []
        self.stack2 = []

    def push(self, x: int) -> None:
        self.stack1.append(x)
        self.stack2.insert(0, x) 

    def pop(self) -> int:
        return self.stack2.pop()

    def peek(self) -> int:
        return self.stack2[-1]

    def empty(self) -> bool:
        if not self.stack2:
            return True
        else:
            return False


